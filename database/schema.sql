-- Online Hospital Management System - Oracle XE 21c schema
-- Run while connected to XEPDB1 as HOSPITAL_APP.

CREATE SEQUENCE PATIENT_NUMBER_SEQ START WITH 1 INCREMENT BY 1 NOCYCLE CACHE 20;

CREATE TABLE ROLES (
    ROLE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ROLE_NAME VARCHAR2(30) NOT NULL,
    DESCRIPTION VARCHAR2(255),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_ROLES PRIMARY KEY (ROLE_ID),
    CONSTRAINT UK_ROLES_NAME UNIQUE (ROLE_NAME),
    CONSTRAINT CK_ROLES_NAME CHECK (ROLE_NAME IN ('ADMIN', 'DOCTOR', 'NURSE', 'RECEPTIONIST', 'PATIENT', 'BILLING_STAFF'))
);

CREATE TABLE USERS (
    USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    USERNAME VARCHAR2(100) NOT NULL,
    PASSWORD_HASH VARCHAR2(255) NOT NULL,
    EMAIL VARCHAR2(255) NOT NULL,
    ROLE_ID NUMBER NOT NULL,
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    FAILED_LOGIN_ATTEMPTS NUMBER(3) DEFAULT 0 NOT NULL,
    LAST_LOGIN_AT TIMESTAMP,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_USERS PRIMARY KEY (USER_ID),
    CONSTRAINT UK_USERS_USERNAME UNIQUE (USERNAME),
    CONSTRAINT UK_USERS_EMAIL UNIQUE (EMAIL),
    CONSTRAINT FK_USERS_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLES (ROLE_ID),
    CONSTRAINT CK_USERS_STATUS CHECK (STATUS IN ('ACTIVE', 'INACTIVE', 'LOCKED')),
    CONSTRAINT CK_USERS_FAILED_LOGIN CHECK (FAILED_LOGIN_ATTEMPTS >= 0)
);

CREATE TABLE DEPARTMENTS (
    DEPARTMENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    DEPARTMENT_NAME VARCHAR2(100) NOT NULL,
    DESCRIPTION VARCHAR2(500),
    LOCATION VARCHAR2(150),
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_DEPARTMENTS PRIMARY KEY (DEPARTMENT_ID),
    CONSTRAINT UK_DEPARTMENTS_NAME UNIQUE (DEPARTMENT_NAME),
    CONSTRAINT CK_DEPARTMENTS_STATUS CHECK (STATUS IN ('ACTIVE', 'INACTIVE'))
);

CREATE TABLE DOCTORS (
    DOCTOR_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    USER_ID NUMBER NOT NULL,
    DEPARTMENT_ID NUMBER NOT NULL,
    REGISTRATION_NUMBER VARCHAR2(50) NOT NULL,
    FIRST_NAME VARCHAR2(100) NOT NULL,
    LAST_NAME VARCHAR2(100) NOT NULL,
    SPECIALIZATION VARCHAR2(150) NOT NULL,
    QUALIFICATION VARCHAR2(255),
    PHONE VARCHAR2(20) NOT NULL,
    CONSULTATION_FEE NUMBER(12,2) DEFAULT 0 NOT NULL,
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_DOCTORS PRIMARY KEY (DOCTOR_ID),
    CONSTRAINT UK_DOCTORS_USER UNIQUE (USER_ID),
    CONSTRAINT UK_DOCTORS_REG_NO UNIQUE (REGISTRATION_NUMBER),
    CONSTRAINT FK_DOCTORS_USER FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
    CONSTRAINT FK_DOCTORS_DEPT FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENTS (DEPARTMENT_ID),
    CONSTRAINT CK_DOCTORS_FEE CHECK (CONSULTATION_FEE >= 0),
    CONSTRAINT CK_DOCTORS_STATUS CHECK (STATUS IN ('ACTIVE', 'INACTIVE'))
);

CREATE TABLE PATIENTS (
    PATIENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    USER_ID NUMBER,
    PATIENT_NUMBER VARCHAR2(50) NOT NULL,
    FIRST_NAME VARCHAR2(100) NOT NULL,
    LAST_NAME VARCHAR2(100) NOT NULL,
    DATE_OF_BIRTH DATE NOT NULL,
    GENDER VARCHAR2(10) NOT NULL,
    BLOOD_GROUP VARCHAR2(5),
    PHONE VARCHAR2(20) NOT NULL,
    EMAIL VARCHAR2(255),
    ADDRESS VARCHAR2(500),
    EMERGENCY_CONTACT_NAME VARCHAR2(200),
    EMERGENCY_CONTACT_PHONE VARCHAR2(20),
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_PATIENTS PRIMARY KEY (PATIENT_ID),
    CONSTRAINT UK_PATIENTS_USER UNIQUE (USER_ID),
    CONSTRAINT UK_PATIENTS_NUMBER UNIQUE (PATIENT_NUMBER),
    CONSTRAINT FK_PATIENTS_USER FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
    CONSTRAINT CK_PATIENTS_GENDER CHECK (GENDER IN ('MALE', 'FEMALE', 'OTHER')),
    CONSTRAINT CK_PATIENTS_STATUS CHECK (STATUS IN ('ACTIVE', 'INACTIVE'))
);

CREATE TABLE ADMISSIONS (
    ADMISSION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PATIENT_ID NUMBER NOT NULL,
    ADMISSION_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    DISCHARGE_DATE TIMESTAMP,
    STATUS VARCHAR2(20) DEFAULT 'ADMITTED' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_ADMISSIONS PRIMARY KEY (ADMISSION_ID),
    CONSTRAINT FK_ADMISSIONS_PATIENT FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS (PATIENT_ID),
    CONSTRAINT CK_ADMISSIONS_STATUS CHECK (STATUS IN ('ADMITTED', 'DISCHARGED', 'CANCELLED'))
);

CREATE INDEX IDX_ADMISSIONS_PATIENT ON ADMISSIONS (PATIENT_ID);

COMMENT ON TABLE ADMISSIONS IS 'Tracks patient admission state used by discharge workflows and safe patient deactivation';
COMMENT ON COLUMN ADMISSIONS.ADMISSION_ID IS 'Identity primary key for an admission';
COMMENT ON COLUMN ADMISSIONS.STATUS IS 'ADMITTED, DISCHARGED, or CANCELLED admission state';

CREATE TABLE DOCTOR_SCHEDULES (
    SCHEDULE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    DOCTOR_ID NUMBER NOT NULL,
    DAY_OF_WEEK VARCHAR2(10) NOT NULL,
    START_TIME VARCHAR2(5) NOT NULL,
    END_TIME VARCHAR2(5) NOT NULL,
    SLOT_DURATION_MINUTES NUMBER(4) NOT NULL,
    IS_AVAILABLE NUMBER(1) DEFAULT 1 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_DOCTOR_SCHEDULES PRIMARY KEY (SCHEDULE_ID),
    CONSTRAINT UK_DOC_SCHED_SLOT UNIQUE (DOCTOR_ID, DAY_OF_WEEK, START_TIME),
    CONSTRAINT FK_DOC_SCHED_DOCTOR FOREIGN KEY (DOCTOR_ID) REFERENCES DOCTORS (DOCTOR_ID),
    CONSTRAINT CK_DOC_SCHED_DAY CHECK (DAY_OF_WEEK IN ('MONDAY', 'TUESDAY', 'WEDNESDAY', 'THURSDAY', 'FRIDAY', 'SATURDAY', 'SUNDAY')),
    CONSTRAINT CK_DOC_SCHED_START CHECK (REGEXP_LIKE(START_TIME, '^([01][0-9]|2[0-3]):[0-5][0-9]$')),
    CONSTRAINT CK_DOC_SCHED_END CHECK (REGEXP_LIKE(END_TIME, '^([01][0-9]|2[0-3]):[0-5][0-9]$')),
    CONSTRAINT CK_DOC_SCHED_RANGE CHECK (END_TIME > START_TIME),
    CONSTRAINT CK_DOC_SCHED_DURATION CHECK (SLOT_DURATION_MINUTES > 0),
    CONSTRAINT CK_DOC_SCHED_AVAILABLE CHECK (IS_AVAILABLE IN (0, 1))
);

CREATE TABLE APPOINTMENTS (
    APPOINTMENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PATIENT_ID NUMBER NOT NULL,
    DOCTOR_ID NUMBER NOT NULL,
    APPOINTMENT_DATE DATE NOT NULL,
    START_TIME VARCHAR2(5) NOT NULL,
    END_TIME VARCHAR2(5) NOT NULL,
    REASON VARCHAR2(500),
    STATUS VARCHAR2(20) DEFAULT 'SCHEDULED' NOT NULL,
    NOTES VARCHAR2(1000),
    CREATED_BY NUMBER NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_APPOINTMENTS PRIMARY KEY (APPOINTMENT_ID),
    CONSTRAINT FK_APPT_PATIENT FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS (PATIENT_ID),
    CONSTRAINT FK_APPT_DOCTOR FOREIGN KEY (DOCTOR_ID) REFERENCES DOCTORS (DOCTOR_ID),
    CONSTRAINT FK_APPT_CREATED_BY FOREIGN KEY (CREATED_BY) REFERENCES USERS (USER_ID),
    CONSTRAINT CK_APPT_START CHECK (REGEXP_LIKE(START_TIME, '^([01][0-9]|2[0-3]):[0-5][0-9]$')),
    CONSTRAINT CK_APPT_END CHECK (REGEXP_LIKE(END_TIME, '^([01][0-9]|2[0-3]):[0-5][0-9]$')),
    CONSTRAINT CK_APPT_TIME_RANGE CHECK (END_TIME > START_TIME),
    CONSTRAINT CK_APPT_STATUS CHECK (STATUS IN ('SCHEDULED', 'CONFIRMED', 'COMPLETED', 'CANCELLED', 'NO_SHOW'))
);

CREATE TABLE MEDICAL_RECORDS (
    MEDICAL_RECORD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PATIENT_ID NUMBER NOT NULL,
    DOCTOR_ID NUMBER NOT NULL,
    APPOINTMENT_ID NUMBER,
    VISIT_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    SYMPTOMS VARCHAR2(2000),
    DIAGNOSIS VARCHAR2(2000),
    TREATMENT_NOTES CLOB,
    ALLERGIES VARCHAR2(1000),
    BLOOD_PRESSURE VARCHAR2(20),
    TEMPERATURE NUMBER(5,2),
    WEIGHT NUMBER(6,2),
    HEIGHT NUMBER(6,2),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_MEDICAL_RECORDS PRIMARY KEY (MEDICAL_RECORD_ID),
    CONSTRAINT UK_MED_REC_APPT UNIQUE (APPOINTMENT_ID),
    CONSTRAINT FK_MED_REC_PATIENT FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS (PATIENT_ID),
    CONSTRAINT FK_MED_REC_DOCTOR FOREIGN KEY (DOCTOR_ID) REFERENCES DOCTORS (DOCTOR_ID),
    CONSTRAINT FK_MED_REC_APPT FOREIGN KEY (APPOINTMENT_ID) REFERENCES APPOINTMENTS (APPOINTMENT_ID),
    CONSTRAINT CK_MED_REC_TEMP CHECK (TEMPERATURE IS NULL OR TEMPERATURE > 0),
    CONSTRAINT CK_MED_REC_WEIGHT CHECK (WEIGHT IS NULL OR WEIGHT > 0),
    CONSTRAINT CK_MED_REC_HEIGHT CHECK (HEIGHT IS NULL OR HEIGHT > 0)
);

CREATE TABLE PRESCRIPTIONS (
    PRESCRIPTION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PATIENT_ID NUMBER NOT NULL,
    DOCTOR_ID NUMBER NOT NULL,
    APPOINTMENT_ID NUMBER,
    MEDICAL_RECORD_ID NUMBER,
    PRESCRIPTION_DATE DATE DEFAULT CURRENT_DATE NOT NULL,
    VALID_UNTIL DATE,
    NOTES VARCHAR2(1000),
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_PRESCRIPTIONS PRIMARY KEY (PRESCRIPTION_ID),
    CONSTRAINT UK_RX_MED_REC UNIQUE (MEDICAL_RECORD_ID),
    CONSTRAINT FK_RX_PATIENT FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS (PATIENT_ID),
    CONSTRAINT FK_RX_DOCTOR FOREIGN KEY (DOCTOR_ID) REFERENCES DOCTORS (DOCTOR_ID),
    CONSTRAINT FK_RX_APPT FOREIGN KEY (APPOINTMENT_ID) REFERENCES APPOINTMENTS (APPOINTMENT_ID),
    CONSTRAINT FK_RX_MED_REC FOREIGN KEY (MEDICAL_RECORD_ID) REFERENCES MEDICAL_RECORDS (MEDICAL_RECORD_ID),
    CONSTRAINT CK_RX_VALID_UNTIL CHECK (VALID_UNTIL IS NULL OR VALID_UNTIL >= PRESCRIPTION_DATE),
    CONSTRAINT CK_RX_STATUS CHECK (STATUS IN ('ACTIVE', 'COMPLETED', 'CANCELLED'))
);

CREATE TABLE PRESCRIPTION_ITEMS (
    PRESCRIPTION_ITEM_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PRESCRIPTION_ID NUMBER NOT NULL,
    MEDICINE_NAME VARCHAR2(200) NOT NULL,
    DOSAGE VARCHAR2(100) NOT NULL,
    FREQUENCY VARCHAR2(100) NOT NULL,
    DURATION VARCHAR2(100) NOT NULL,
    ROUTE VARCHAR2(100),
    INSTRUCTIONS VARCHAR2(500),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_PRESCRIPTION_ITEMS PRIMARY KEY (PRESCRIPTION_ITEM_ID),
    CONSTRAINT FK_RX_ITEM_RX FOREIGN KEY (PRESCRIPTION_ID) REFERENCES PRESCRIPTIONS (PRESCRIPTION_ID)
);

CREATE TABLE BILLS (
    BILL_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    BILL_NUMBER VARCHAR2(50) NOT NULL,
    PATIENT_ID NUMBER NOT NULL,
    APPOINTMENT_ID NUMBER,
    BILL_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    SUBTOTAL NUMBER(14,2) DEFAULT 0 NOT NULL,
    TAX_AMOUNT NUMBER(14,2) DEFAULT 0 NOT NULL,
    DISCOUNT_AMOUNT NUMBER(14,2) DEFAULT 0 NOT NULL,
    TOTAL_AMOUNT NUMBER(14,2) DEFAULT 0 NOT NULL,
    PAID_AMOUNT NUMBER(14,2) DEFAULT 0 NOT NULL,
    BALANCE_AMOUNT NUMBER(14,2) DEFAULT 0 NOT NULL,
    STATUS VARCHAR2(20) DEFAULT 'UNPAID' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_BILLS PRIMARY KEY (BILL_ID),
    CONSTRAINT UK_BILLS_NUMBER UNIQUE (BILL_NUMBER),
    CONSTRAINT FK_BILLS_PATIENT FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS (PATIENT_ID),
    CONSTRAINT FK_BILLS_APPT FOREIGN KEY (APPOINTMENT_ID) REFERENCES APPOINTMENTS (APPOINTMENT_ID),
    CONSTRAINT CK_BILLS_AMOUNTS CHECK (SUBTOTAL >= 0 AND TAX_AMOUNT >= 0 AND DISCOUNT_AMOUNT >= 0 AND TOTAL_AMOUNT >= 0 AND PAID_AMOUNT >= 0 AND BALANCE_AMOUNT >= 0),
    CONSTRAINT CK_BILLS_STATUS CHECK (STATUS IN ('UNPAID', 'PARTIALLY_PAID', 'PAID', 'CANCELLED', 'REFUNDED'))
);

CREATE SEQUENCE BILL_NUMBER_SEQ START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;

CREATE TABLE BILL_ITEMS (
    BILL_ITEM_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    BILL_ID NUMBER NOT NULL,
    ITEM_TYPE VARCHAR2(20) NOT NULL,
    DESCRIPTION VARCHAR2(500) NOT NULL,
    QUANTITY NUMBER(10,2) NOT NULL,
    UNIT_PRICE NUMBER(14,2) NOT NULL,
    LINE_TOTAL NUMBER(14,2) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_BILL_ITEMS PRIMARY KEY (BILL_ITEM_ID),
    CONSTRAINT FK_BILL_ITEMS_BILL FOREIGN KEY (BILL_ID) REFERENCES BILLS (BILL_ID),
    CONSTRAINT CK_BILL_ITEMS_QTY CHECK (QUANTITY > 0),
    CONSTRAINT CK_BILL_ITEMS_AMOUNTS CHECK (UNIT_PRICE >= 0 AND LINE_TOTAL >= 0),
    CONSTRAINT CK_BILL_ITEMS_TYPE CHECK (ITEM_TYPE IN ('CONSULTATION', 'ROOM', 'MEDICINE', 'LAB_TEST', 'PROCEDURE', 'OTHER'))
);

CREATE TABLE PAYMENTS (
    PAYMENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PAYMENT_NUMBER VARCHAR2(50) NOT NULL,
    BILL_ID NUMBER NOT NULL,
    PAYMENT_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    AMOUNT NUMBER(14,2) NOT NULL,
    PAYMENT_METHOD VARCHAR2(20) NOT NULL,
    TRANSACTION_REFERENCE VARCHAR2(150),
    STATUS VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    RECEIVED_BY NUMBER NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_PAYMENTS PRIMARY KEY (PAYMENT_ID),
    CONSTRAINT UK_PAYMENTS_NUMBER UNIQUE (PAYMENT_NUMBER),
    CONSTRAINT FK_PAYMENTS_BILL FOREIGN KEY (BILL_ID) REFERENCES BILLS (BILL_ID),
    CONSTRAINT FK_PAYMENTS_RECEIVER FOREIGN KEY (RECEIVED_BY) REFERENCES USERS (USER_ID),
    CONSTRAINT CK_PAYMENTS_AMOUNT CHECK (AMOUNT > 0),
    CONSTRAINT CK_PAYMENTS_METHOD CHECK (PAYMENT_METHOD IN ('CASH', 'CARD', 'UPI', 'BANK_TRANSFER', 'INSURANCE')),
    CONSTRAINT CK_PAYMENTS_STATUS CHECK (STATUS IN ('PENDING', 'SUCCESS', 'FAILED', 'REFUNDED'))
);

CREATE SEQUENCE PAYMENT_NUMBER_SEQ START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;

CREATE TABLE AUDIT_LOGS (
    AUDIT_LOG_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    USER_ID NUMBER,
    ACTION VARCHAR2(100) NOT NULL,
    ENTITY_NAME VARCHAR2(100) NOT NULL,
    ENTITY_ID NUMBER,
    OLD_VALUE CLOB,
    NEW_VALUE CLOB,
    IP_ADDRESS VARCHAR2(45),
    USER_AGENT VARCHAR2(500),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_AUDIT_LOGS PRIMARY KEY (AUDIT_LOG_ID),
    CONSTRAINT FK_AUDIT_LOGS_USER FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID)
);

CREATE INDEX IDX_USERS_ROLE ON USERS (ROLE_ID);
