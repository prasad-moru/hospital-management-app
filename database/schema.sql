CREATE UNIQUE INDEX UK_APPT_ACTIVE_DOCTOR_SLOT ON APPOINTMENTS (
    CASE WHEN STATUS IN ('SCHEDULED', 'CONFIRMED') THEN DOCTOR_ID END,
    CASE WHEN STATUS IN ('SCHEDULED', 'CONFIRMED') THEN APPOINTMENT_DATE END,
    CASE WHEN STATUS IN ('SCHEDULED', 'CONFIRMED') THEN START_TIME END
);
