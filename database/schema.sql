CREATE INDEX IDX_MED_REC_DOCTOR ON MEDICAL_RECORDS (DOCTOR_ID);
CREATE INDEX IDX_MED_REC_APPOINTMENT ON MEDICAL_RECORDS (APPOINTMENT_ID);
CREATE INDEX IDX_MED_REC_VISIT_DATE ON MEDICAL_RECORDS (VISIT_DATE);
CREATE INDEX IDX_RX_PATIENT ON PRESCRIPTIONS (PATIENT_ID);
CREATE INDEX IDX_RX_DOCTOR ON PRESCRIPTIONS (DOCTOR_ID);
CREATE INDEX IDX_RX_MED_REC ON PRESCRIPTIONS (MEDICAL_RECORD_ID);
CREATE INDEX IDX_RX_DATE ON PRESCRIPTIONS (PRESCRIPTION_DATE);
CREATE INDEX IDX_RX_STATUS ON PRESCRIPTIONS (STATUS);
CREATE INDEX IDX_BILLS_PATIENT ON BILLS (PATIENT_ID);
CREATE INDEX IDX_BILLS_STATUS ON BILLS (STATUS);
CREATE INDEX IDX_BILLS_APPT ON BILLS (APPOINTMENT_ID);
CREATE INDEX IDX_BILLS_DATE ON BILLS (BILL_DATE);
CREATE INDEX IDX_PAYMENTS_BILL ON PAYMENTS (BILL_ID);
CREATE INDEX IDX_PAYMENTS_DATE ON PAYMENTS (PAYMENT_DATE);
CREATE INDEX IDX_PAYMENTS_STATUS ON PAYMENTS (STATUS);
CREATE INDEX IDX_AUDIT_USER ON AUDIT_LOGS (USER_ID);
CREATE INDEX IDX_AUDIT_CREATED ON AUDIT_LOGS (CREATED_AT);

